package chapter2_review;

public class Test7_review {

	public static void main(String[] args) {
		int a = 10;
//		a++ : 전위 1 증가 / ++a : 후위 1 증가
		a++;
		System.out.println(a);
		++a;
		System.out.println(a);
		
		int b = 10;
		int c = 10;
		System.out.println(++b);
		System.out.println(c++);

	}

}
